package dao;

import javax.ejb.Stateless;

/**
 *
 * @author Familia
 */
@Stateless
public class Usuario {
   
   private String Nombre, Contraseña,Apellido,Direccion,Correo;
   private int    Cedula, Edad,Telefono;
   
    public Usuario() {
    }


    public Usuario(int Cedula, String Contraseña) {
        this.Cedula = Cedula;
        this.Contraseña = Contraseña;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public String getContraseña() {
        return Contraseña;
    }

    public void setContraseñaU(String ContraseñaU) {
        this.Contraseña = ContraseñaU;
    }

    public int getCedula() {
        return Cedula;
    }

    public void setCedula(int Cedula) {
        this.Cedula = Cedula;
    }

    public int getEdad() {
        return Edad;
    }

    public void setEdad(int Edad) {
        this.Edad = Edad;
    }

    public String getApellido() {
        return Apellido;
    }

    public void setApellido(String Apellido) {
        this.Apellido = Apellido;
    }

    public String getDireccion() {
        return Direccion;
    }

    public void setDireccion(String Direccion) {
        this.Direccion = Direccion;
    }

    public String getCorreo() {
        return Correo;
    }

    public void setCorreo(String Correo) {
        this.Correo = Correo;
    }

    public int getTelefono() {
        return Telefono;
    }

    public void setTelefono(int Telefono) {
        this.Telefono = Telefono;
    }

   
    
    
    
}
